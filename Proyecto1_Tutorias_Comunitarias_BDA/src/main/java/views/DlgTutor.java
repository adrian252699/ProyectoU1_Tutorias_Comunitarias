    private void cargarTablaTutores(){
        tblTutor.setModel(tuController.obtenerTablaTutores());
    }
    private void buscar() {
        String nombre = txtFiltro.getText().trim();
        if (nombre.isEmpty()) {
            cargarTablaTutores();
        } else {
            tblTutor.setModel(tuController.obtenerTablaTutoresFiltro(nombre));
        }
    }
    
    public Integer getTutorSeleccionado() {
        int fila = tblTutor.getSelectedRow();
        if (fila >= 0) {
            return (Integer) tblTutor.getValueAt(fila, 0); // columna 0 = id
        }
        return null;
    }
    

