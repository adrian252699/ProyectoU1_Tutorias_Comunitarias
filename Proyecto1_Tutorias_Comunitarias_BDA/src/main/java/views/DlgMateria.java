    private void cargarTablaMaterias(){
        tblMateria.setModel(maController.obtenerTablaMaterias());
    }
    
    private void buscar() {
        String nombre = txtFiltro.getText().trim();
        if (nombre.isEmpty()) {
            cargarTablaMaterias();
        } else {
            tblMateria.setModel(maController.obtenerTablaMateriasFiltro(nombre));
        }
    }
    
    public Integer getMateriaSeleccionada() {
        int fila = tblMateria.getSelectedRow();
        if (fila >= 0) {
            return (Integer) tblMateria.getValueAt(fila, 0); // columna 0 = id
        }
        return null;
    }

